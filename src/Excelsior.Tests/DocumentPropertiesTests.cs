using DocumentFormat.OpenXml.CustomProperties;
using DocumentFormat.OpenXml.Packaging;

[TestFixture]
public class DocumentPropertiesTests
{
    [Test]
    public async Task RoundTrip_DocumentProperties()
    {
        #region DocumentPropertiesUsage

        var builder = new BookBuilder();
        builder.AddSheet(SampleData.Employees());
        builder.SetProperties(
            new()
            {
                Title = "Q1 staff snapshot",
                Author = "Simon",
                Subject = "Headcount",
                Keywords = "staff, q1",
                Comments = "Generated by Excelsior",
                Category = "HR",
                Status = "Final",
                LastModifiedBy = "Simon",
                Company = "Papyrine",
                Manager = "Jane",
                Custom =
                {
                    ["Project"] = "Excelsior",
                    ["Reviewed"] = true,
                    ["Revision"] = 3,
                    ["Score"] = 9.5,
                    ["GeneratedAt"] = new DateTime(2026, 1, 15, 9, 30, 0, DateTimeKind.Utc)
                }
            });

        using var stream = await builder.ToMemoryStream();

        #endregion

        // ToMemoryStream clones the package, so reading the reopened stream proves the
        // properties survive the clone rather than only existing on the in-memory document.
        using var document = SpreadsheetDocument.Open(stream, false);

        var core = document.PackageProperties;
        Assert.Multiple(() =>
        {
            Assert.That(core.Title, Is.EqualTo("Q1 staff snapshot"));
            Assert.That(core.Creator, Is.EqualTo("Simon"));
            Assert.That(core.Subject, Is.EqualTo("Headcount"));
            Assert.That(core.Keywords, Is.EqualTo("staff, q1"));
            Assert.That(core.Description, Is.EqualTo("Generated by Excelsior"));
            Assert.That(core.Category, Is.EqualTo("HR"));
            Assert.That(core.ContentStatus, Is.EqualTo("Final"));
            Assert.That(core.LastModifiedBy, Is.EqualTo("Simon"));
        });

        var extended = document.ExtendedFilePropertiesPart!.Properties!;
        Assert.Multiple(() =>
        {
            Assert.That(extended.Company!.Text, Is.EqualTo("Papyrine"));
            Assert.That(extended.Manager!.Text, Is.EqualTo("Jane"));
        });

        var custom = document.CustomFilePropertiesPart!.Properties!
            .Elements<CustomDocumentProperty>()
            .ToDictionary(_ => _.Name!.Value!, _ => _.InnerText);
        Assert.Multiple(() =>
        {
            Assert.That(custom["Project"], Is.EqualTo("Excelsior"));
            Assert.That(custom["Reviewed"], Is.EqualTo("true"));
            Assert.That(custom["Revision"], Is.EqualTo("3"));
            Assert.That(custom["Score"], Is.EqualTo("9.5"));
            Assert.That(custom["GeneratedAt"], Is.EqualTo("2026-01-15T09:30:00Z"));
        });
    }

    [Test]
    public async Task SetProperties_LastCallWins()
    {
        var builder = new BookBuilder();
        builder.AddSheet(SampleData.Employees());
        builder.SetProperties(new() { Title = "first" });
        builder.SetProperties(new() { Title = "second" });

        using var stream = await builder.ToMemoryStream();
        using var document = SpreadsheetDocument.Open(stream, false);

        Assert.That(document.PackageProperties.Title, Is.EqualTo("second"));
    }

    [Test]
    public void SetProperties_UnsupportedCustomType_Throws()
    {
        var builder = new BookBuilder();
        builder.AddSheet(SampleData.Employees());
        builder.SetProperties(
            new()
            {
                Custom =
                {
                    ["Bad"] = new()
                }
            });

        // The unsupported value is only encountered when the workbook is built.
        var exception = Assert.ThrowsAsync<ArgumentException>(async () => await builder.ToMemoryStream());
        Assert.That(exception!.Message, Does.Contain("Bad").And.Contain("System.Object"));
    }
}
